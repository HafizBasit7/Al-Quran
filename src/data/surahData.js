export const getSurahImages = (surahNumber) => {
    const surah = surahData.find(s => s.number === surahNumber);
    if (!surah) return [];

    // Import the existing page images
    const { quranPages } = require('./quranImages');

    // Calculate the slice indices
    const startIndex = surah.pageStart - 1; // Convert to 0-based index
    const endIndex = surah.pageEnd - 1;     // Convert to 0-based index

    // Return the slice of pages for this surah
    return quranPages.slice(startIndex, endIndex + 1);
};